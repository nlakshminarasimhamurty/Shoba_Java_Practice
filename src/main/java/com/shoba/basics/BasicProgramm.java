package com.shoba.basics;

public class BasicProgramm {

	public static void main(String[] args) {

		System.out.println("Hi"); // String
		System.out.println('h'); // Charecter
		System.out.println(123); // integer
		System.out.println("123@%^&*"); // String
		System.out.println('3'); // charecter

		System.out.println("Hi " + "Good Morning"); // concatination

		System.out.println("Hi" + 123 + 1); // hi1231

		System.out.println(1 + 2 + "hi"); // 3hi
		System.out.println("Hi" + (12 + 1)); //

		System.out.println('a' + 1);
		System.out.println('a' + 'b');

		System.out.println('a' + "hi" + 12); // ahi12
		System.out.println('a' + 2 + "hi" + 4); //
		
		System.out.println("Naveen"+"@$5%");
		System.out.println("Kumar");
		System.out.println('@' +'$');
		
		// Single line comment 
		
		/* Naveen 
		 * 
		 * 
		 * 
		 * 
		 */
		
		/**
		 * 
		 * 
		 * 
		 * */
		
				
	}

}
